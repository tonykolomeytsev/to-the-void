    private class Dust extends GameObject {

        private float alpha, radius, radiusIncrement, vx, vy;
        private boolean alive;

        public Dust() {
            init();
        }

        public Dust(float x, float y, float alpha, float radius, float radiusIncrement, float vx, float vy) {
            this.x = x;
            this.y = y;
            this.alpha = alpha;
            this.radius = radius;
            this.radiusIncrement = radiusIncrement;
            this.vx = vx;
            this.vy = vy;
        }

        @Override
        public void init() {
            Random random = new Random();
            (paint = new Paint()).setColor(Color.WHITE);
            paint.setAntiAlias(true);
            this.x = Config.RENDER_WIDTH / 2;
            vx = ((random.nextFloat() * 2f) - 1f) * 2f;
            this.y = Config.RENDER_HEIGHT / 2;
            vy = ((random.nextFloat() * 2f) - 1f) * 2f;
            this.alpha = 0f;
            this.radius = 0f;
            this.radiusIncrement = random.nextFloat();
            alive = true;
        }

        @Override
        public void update(float delta) {
            alpha = (alpha < 1f) ? (alpha + delta) : 1f;
            paint.setAlpha((int) (alpha * 255));
            radius += radiusIncrement * delta * 0.2f;
            x += vx * delta * 5f;
            vx *= 1f + (delta * 0.3f);
            y += vy * delta * 5f;
            vy *= 1f + (delta * 0.3f);
            if (x < 0 || x > Config.RENDER_WIDTH || y < 0 || y > Config.RENDER_HEIGHT)
                alive = false;
        }

        public boolean isAlive() {
            return alive;
        }

        @Override
        public void render(Canvas canvas) {
            canvas.drawCircle(x, y, radius, paint);
        }
    }


